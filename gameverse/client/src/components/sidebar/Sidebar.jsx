import React, { useState, useEffect } from "react";
import { useNavigate } from "react-router-dom";

function Sidebar({ isCollapsed, setIsCollapsed, onLogout }) {
  const navigate = useNavigate();
  const [openMenu, setOpenMenu] = useState({
    games: false,
    settings: false,
  });

  useEffect(() => {
    if (isCollapsed) {
      setOpenMenu({ games: false, settings: false });
    }
  }, [isCollapsed]);

  const toggleMenu = (menu) => {
    if (isCollapsed) {
      setIsCollapsed(false);
    }
    setOpenMenu((prev) => ({
      ...prev,
      [menu]: !prev[menu],
    }));
  };

  const handleLogout = async () => {
    try {
      const response = await fetch("http://localhost:5000/logout", {
        method: "POST",
        credentials: "include",
      });

      if (response.ok) {
        onLogout();
      } else {
        console.error("Failed to log out.");
      }
    } catch (error) {
      console.error("Logout error:", error);
    }
  };

  return (
    <nav className={`sidebar ${isCollapsed ? "collapsed" : ""}`}>
      <ul className="menu-list no-bullets">
        <li className="menu-item">
          <button
            className="menu-btn toggle-btn no-arrow"
            onClick={() => navigate("/")}
          >
            {isCollapsed ? "H" : "Home"}
          </button>
        </li>

        {/* GAMES */}
        <li className={`menu-item has-submenu ${openMenu.games ? "open" : ""}`}>
          <button
            className="menu-btn toggle-btn"
            onClick={() => toggleMenu("games")}
          >
            {isCollapsed ? "üéÆ" : "Singleplayer"}
          </button>
          <ul
            className="submenu"
            style={{ display: openMenu.games ? "block" : "none" }}
          >
            <li>
              <button
                className="menu-btn"
                onClick={() => navigate("/tic-tac-toe")}
              >
                Tic-Tac-Toe
              </button>
            </li>
            <li>
              <button className="menu-btn" onClick={() => navigate("/simon")}>
                Simon Game
              </button>
            </li>
            <li>
              <button className="menu-btn" onClick={() => navigate("/pexeso")}>
                Pexeso
              </button>
            </li>
            <li>
              <button
                className="menu-btn"
                onClick={() => navigate("/zombiearena")}
              >
                Zombie Arena
              </button>
            </li>
          </ul>
        </li>

        {/* STATIC ITEMS */}
        <li className="menu-item">
          <button
            className="menu-btn toggle-btn no-arrow"
            onClick={() => navigate("/multiplayer")}
          >
            {isCollapsed ? "üÜö" : "Multiplayer (TBD)"}
          </button>
        </li>
        <li className="menu-item">
          <button
            className="menu-btn toggle-btn no-arrow"
            onClick={() => navigate("/leaderboard")}
          >
            {isCollapsed ? "üèÜ" : "Leaderboards"}
          </button>
        </li>
        <li className="menu-item">
          <button
            className="menu-btn toggle-btn no-arrow"
            onClick={() => navigate("/character-creation")}
          >
            {isCollapsed ? "üë§" : "Char Creation"}
          </button>
        </li>
        <li className="menu-item">
          <button
            className="menu-btn toggle-btn no-arrow"
            onClick={() => navigate("/profile")}
          >
            {isCollapsed ? "ü™™" : "Player Profile"}
          </button>
        </li>

        {/* SETTINGS */}
        <li
          className={`menu-item has-submenu ${openMenu.settings ? "open" : ""}`}
        >
          <button
            className="menu-btn toggle-btn"
            onClick={() => toggleMenu("settings")}
          >
            {isCollapsed ? "‚öôÔ∏è" : "Settings"}
          </button>
          <ul
            className="submenu"
            style={{ display: openMenu.settings ? "block" : "none" }}
          >
            <li>
              <button
                className="menu-btn"
                onClick={() => navigate("/settings/account")}
              >
                Change Password
              </button>
            </li>
            <li>
              <button
                className="menu-btn"
                onClick={() => navigate("/settings/username")}
              >
                Change Username
              </button>
            </li>
            <li>
              <button
                className="menu-btn"
                onClick={() => navigate("/settings/delete-account")}
              >
                Delete Account
              </button>
            </li>
          </ul>
        </li>

        {/* LOGOUT */}
        <li className="menu-item">
          <button
            className="menu-btn toggle-btn no-arrow"
            onClick={handleLogout}
          >
            {isCollapsed ? "üö™" : "Log Out"}
          </button>
        </li>
      </ul>
    </nav>
  );
}

export default Sidebar;
